package com.github.tudeteam.telegram.thefreestuffbot.structures;

/**
 * Represents the format to display the until date in.
 */
public enum UntilFormat {
    /**
     * Display as a specific date (until 2020-08-23).
     */
    DATE,

    /**
     * Display as the day of the week (until next Sunday).
     */
    WEEKDAY
}
